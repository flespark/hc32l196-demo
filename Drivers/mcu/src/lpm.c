/**
 *****************************************************************************
 ** \brief 进入深度睡眠模式
 **
 ** \input bOnExit - TRUE:当退出异常处理后，自动再次进入休眠；
 **                  FALSE：唤醒后不再自动进入休眠
 ** 
 ** \retval NULL                                     
 *****************************************************************************/
void Lpm_GotoDeepSleep(boolean_t bOnExit)
{
    SCB->SCR |= SCB_SCR_SLEEPDEEP_Msk;
    SCB->SCR |= 1u<<bOnExit;
    __WFI();
}

/**
 *****************************************************************************
 ** \brief 进入普通睡眠模式
 **
 ** \input bOnExit - TRUE:当退出异常处理后，自动再次进入休眠；
 **                  FALSE：唤醒后不再自动进入休眠
 ** 
 ** \retval NULL                                     
 *****************************************************************************/
void Lpm_GotoSleep(boolean_t bOnExit)
{
    SCB->SCR &= ~SCB_SCR_SLEEPDEEP_Msk;
    SCB->SCR |= 1u<<bOnExit;
    __WFI();
}
                        
