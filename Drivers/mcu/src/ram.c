/**
 *****************************************************************************
 ** \brief Ram奇偶校验出错地址获取
 **
 ** 
 ** \retval ERROR ADDRESS                                    
 *****************************************************************************/
uint32_t Ram_ErrAddrGet(void)
{    
    return M0P_RAM->ERRADDR;
}

/**
 *****************************************************************************
 ** \brief Ram中断标志获取
 **
 ** 
 ** \retval TRUE or FALSE                                     
 *****************************************************************************/
boolean_t Ram_GetIntFlag(void)
{    
    if(M0P_RAM->IFR & 0x1)
    {
        return TRUE;
    }
    else
    {
        return FALSE;
    }
}

/**
 *****************************************************************************
 ** \brief Ram中断标志清除
 **
 **
 ** \retval Null                                      
 *****************************************************************************/
void Ram_ClearIntFlag(void)
{
    M0P_RAM->ICLR = 0u;    
}

/**
 *****************************************************************************
 ** \brief Ram中断使能
 ** 
 ** \retval Null                                     
 *****************************************************************************/
void Ram_EnableIrq (void)
{
    M0P_RAM->CR |= 0x2u;  
}

/**
 *****************************************************************************
 ** \brief ram中断禁止
 ** 
 ** \retval Null                                      
 *****************************************************************************/
void Ram_DisableIrq(void)
{
    M0P_RAM->CR &= 0x1;
}


